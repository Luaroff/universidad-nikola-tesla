package beans;

public class EstudianteDTO {

	private int ideEst;
	private String nomEst, apeEst, dniEst, genEst, fecEst, emaEst, telEst, dirEst;

	public int getIdeEst() {
		return ideEst;
	}

	public void setIdeEst(int ideEst) {
		this.ideEst = ideEst;
	}

	public String getNomEst() {
		return nomEst;
	}

	public void setNomEst(String nomEst) {
		this.nomEst = nomEst;
	}

	public String getApeEst() {
		return apeEst;
	}

	public void setApeEst(String apeEst) {
		this.apeEst = apeEst;
	}

	public String getDniEst() {
		return dniEst;
	}

	public void setDniEst(String dniEst) {
		this.dniEst = dniEst;
	}

	public String getGenEst() {
		return genEst;
	}

	public void setGenEst(String genEst) {
		this.genEst = genEst;
	}

	public String getFecEst() {
		return fecEst;
	}

	public void setFecEst(String fecEst) {
		this.fecEst = fecEst;
	}

	public String getEmaEst() {
		return emaEst;
	}

	public void setEmaEst(String emaEst) {
		this.emaEst = emaEst;
	}

	public String getTelEst() {
		return telEst;
	}

	public void setTelEst(String telEst) {
		this.telEst = telEst;
	}

	public String getDirEst() {
		return dirEst;
	}

	public void setDirEst(String dirEst) {
		this.dirEst = dirEst;
	}

}
