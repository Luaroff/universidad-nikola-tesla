package beans;

public class MatriculaDTO {

	private int numMatricula;
	private String fecMatricula;
	private int codAdmin, codEstudiante, codCarrera, codTurno, codSede;
	private String nomAdmin, nomEstudiante, nomCarrera, nomTurno, nomSede;

	public int getNumMatricula() {
		return numMatricula;
	}

	public void setNumMatricula(int numMatricula) {
		this.numMatricula = numMatricula;
	}

	public String getFecMatricula() {
		return fecMatricula;
	}

	public void setFecMatricula(String fecMatricula) {
		this.fecMatricula = fecMatricula;
	}

	public int getCodAdmin() {
		return codAdmin;
	}

	public void setCodAdmin(int codAdmin) {
		this.codAdmin = codAdmin;
	}

	public int getCodEstudiante() {
		return codEstudiante;
	}

	public void setCodEstudiante(int codEstudiante) {
		this.codEstudiante = codEstudiante;
	}

	public int getCodCarrera() {
		return codCarrera;
	}

	public void setCodCarrera(int codCarrera) {
		this.codCarrera = codCarrera;
	}

	public int getCodTurno() {
		return codTurno;
	}

	public void setCodTurno(int codTurno) {
		this.codTurno = codTurno;
	}

	public int getCodSede() {
		return codSede;
	}

	public void setCodSede(int codSede) {
		this.codSede = codSede;
	}

	public String getNomAdmin() {
		return nomAdmin;
	}

	public void setNomAdmin(String nomAdmin) {
		this.nomAdmin = nomAdmin;
	}

	public String getNomEstudiante() {
		return nomEstudiante;
	}

	public void setNomEstudiante(String nomEstudiante) {
		this.nomEstudiante = nomEstudiante;
	}

	public String getNomCarrera() {
		return nomCarrera;
	}

	public void setNomCarrera(String nomCarrera) {
		this.nomCarrera = nomCarrera;
	}

	public String getNomTurno() {
		return nomTurno;
	}

	public void setNomTurno(String nomTurno) {
		this.nomTurno = nomTurno;
	}

	public String getNomSede() {
		return nomSede;
	}

	public void setNomSede(String nomSede) {
		this.nomSede = nomSede;
	}

}
